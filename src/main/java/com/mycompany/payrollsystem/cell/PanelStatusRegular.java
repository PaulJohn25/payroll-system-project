    public void setStatus(String status) {
        
        status = status.trim();
        
        switch (status) {
            case "Regular" -> {
                setupStatusPanel(STATUS_REGULAR_PANEL_COLOR, STATUS_REGULAR_FOREGROUND_COLOR, status);
            }
            case "One Time" -> {
                setupStatusPanel(STATUS_ONE_TIME_PANEL_COLOR, STATUS_ONE_TIME_FOREGROUND_COLOR, status);
            }
            default -> {
                setupStatusPanel(new Color(255, 0 ,0), new Color(255, 255, 255), "Error");
            }    
        }
    }
    
    private void setupStatusPanel(Color panelColor, Color foregroundColor, String status) {
        statusPanel.setBackground(panelColor);
        statusText.setText(status);
        statusText.setForeground(foregroundColor);
    }
    
